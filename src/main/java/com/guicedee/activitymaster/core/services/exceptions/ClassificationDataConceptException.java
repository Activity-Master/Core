package com.guicedee.activitymaster.core.services.exceptions;

public class ClassificationDataConceptException
		extends ActivityMasterException
{
	public ClassificationDataConceptException()
	{
	}

	public ClassificationDataConceptException(String message)
	{
		super(message);
	}

	public ClassificationDataConceptException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public ClassificationDataConceptException(Throwable cause)
	{
		super(cause);
	}

	public ClassificationDataConceptException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
	{
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
